void main(){
    int a = 10;
    int b = 15;
    
    if(a > b){
        print("a is greater");
    }
    else if(a < b){
        print("b is greater");
    }
    else{
        print("a and b are equal");
    }

    print("The value of a is : ${a}");
    print("The value of b is : ${b}");
}